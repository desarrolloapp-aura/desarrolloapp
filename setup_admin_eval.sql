-- Create table for admin evaluations
create table if not exists evaluaciones_admin (
  id bigint generated by default as identity primary key,
  proveedor text not null,
  item text not null,
  valor integer not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Add unique constraint to avoid duplicates per provider/item
alter table evaluaciones_admin add constraint evaluaciones_admin_proveedor_item_key unique (proveedor, item);

-- Enable RLS
alter table evaluaciones_admin enable row level security;

-- Policy (adjust as needed, currently open for demo)
create policy "Enable all access for all users" on evaluaciones_admin for all using (true) with check (true);
